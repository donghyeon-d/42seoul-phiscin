int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	if (nb < 2)
		return (0);
	if (i < nb)
	{
		j = 1;
		while (i * j != nb && i * j < nb)
		{
			j++;
		}
		if (i * j == nb)
			return (0);
		i++;
	}
	return (1);
}
